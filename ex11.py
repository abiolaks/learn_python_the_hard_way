# Taking input and asking questions from users


# asking user to enter 
print("How old are you?", end=' ')

# the variable to hold the age inputted by the user
age = input()

# asking the user to enter height 
print("How tall are you?", end=' ')

# the variable height to hold the value height entered by the user
height = input()

# asking the user to enter weight
print("How much do you weight?", end=' ')

# a variable holding  the weight of the user
weight = input()

# displaying formatted output
print(f"So, you're {age} old, {height} tall and {weight} heavy.")
